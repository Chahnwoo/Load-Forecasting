def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)

def parse_yyyy_mm_dd(s: str) -> date:
    return datetime.strptime(s, "%Y-%m-%d").date()

def utc_hourly_index(start_date: date, end_date: date) -> pd.DatetimeIndex:
    """Return a tz-aware UTC DatetimeIndex covering every hour of [start_date, end_date]."""
    start = pd.Timestamp(start_date.strftime("%Y-%m-%d 00:00:00"), tz="UTC")
    end = pd.Timestamp(end_date.strftime("%Y-%m-%d 23:00:00"), tz="UTC")
    return pd.date_range(start=start, end=end, freq="h", tz="UTC")

def time_key_utc(ts_utc: pd.Timestamp) -> str:
    """Format a UTC timestamp (tz-aware or naive) as YYYY-MM-DD:HH."""
    ts = pd.to_datetime(ts_utc)
    if ts.tzinfo is not None:
        ts = ts.tz_convert("UTC").tz_localize(None)
    return ts.strftime("%Y-%m-%d:%H")

def c_to_f(x: pd.Series) -> pd.Series:
    return x.astype(float) * 9.0 / 5.0 + 32.0

def http_get_json(url: str, params: dict, *, timeout: int = 60, max_retries: int = 3) -> dict:
    last = None
    for attempt in range(max_retries):
        try:
            r = requests.get(url, params=params, timeout=timeout, headers={"User-Agent": "load-forecasting/1.0"})
            r.raise_for_status()
            return r.json()
        except Exception as e:
            last = e
            time.sleep(1.0 * (attempt + 1))
    raise RuntimeError(f"GET failed: {url} params={params}") from last

def http_get_bytes(url: str, params: dict, *, timeout: int = 120, max_retries: int = 3) -> bytes:
    last = None
    for attempt in range(max_retries):
        try:
            r = requests.get(url, params=params, timeout=timeout, headers={"User-Agent": "load-forecasting/1.0"})
            r.raise_for_status()
            return r.content
        except Exception as e:
            last = e
            time.sleep(1.0 * (attempt + 1))
    raise RuntimeError(f"GET failed: {url} params={params}") from last


