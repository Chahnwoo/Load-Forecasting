def fetch_forecast_for_station(station: Station, *, start_date: date, end_date: date) -> pd.DataFrame:
    params = {
        "latitude": station.latitude,
        "longitude": station.longitude,
        "start_date": start_date.strftime("%Y-%m-%d"),
        "end_date": end_date.strftime("%Y-%m-%d"),
        "hourly": ",".join(WEATHER_HOURLY_VARS),
        "timezone": TZ_UTC,
    }
    js = http_get_json(OPEN_METEO_FORECAST_URL, params=params, timeout=90)
    hourly = js.get("hourly") or {}
    times = hourly.get("time") or []
    if not times:
        raise RuntimeError(f"No hourly forecast returned for station={station.name}")

    # Open-Meteo returns ISO-8601 strings in UTC when timezone="UTC" is requested.
    # Parse them as UTC-aware so reindexing is unambiguous at DST boundaries.
    df = pd.DataFrame({"time_utc": pd.to_datetime(times, utc=True)})
    for v in WEATHER_HOURLY_VARS:
        df[v] = hourly.get(v)

    full_idx = utc_hourly_index(start_date, end_date)  # tz-aware UTC
    df = df.set_index("time_utc").reindex(full_idx).reset_index().rename(columns={"index": "time_utc"})
    return df

def collect_weighted_region_weather_forecast(
    region: str,
    *,
    start_date: date,
    end_date: date,
    allow_fallback: bool = False,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """
    Uses station weights CSV by default. If missing and allow_fallback=True,
    falls back to geocoding/deterministic coords.
    """
    try:
        stations, weights, meta = collect_region_stations_and_weights_from_csv(
            region, start_date=start_date, end_date=end_date, max_stations=5, weights_csv=STATION_WEIGHTS_CSV
        )
        meta["source"] = meta.get("source", "stations_population_weights_csv")
    except Exception as e:
        if not allow_fallback:
            raise
        print(f"[warn] using fallback stations for region={region} because weights CSV failed: {type(e).__name__}: {e}")
        stations, weights, meta = collect_region_stations_fallback(region, start_date=start_date, end_date=end_date, max_stations=5)

    station_dfs = [fetch_forecast_for_station(s, start_date=start_date, end_date=end_date) for s in stations]

    # Seed the accumulator with a tz-aware UTC index so merges at DST boundaries are unambiguous.
    out = pd.DataFrame({"time_utc": utc_hourly_index(start_date, end_date)})
    for v in WEATHER_HOURLY_VARS:
        out[v] = 0.0

    for df_s, w in zip(station_dfs, weights):
        merged = out.merge(df_s, on="time_utc", how="left", suffixes=("", "_s"))
        for v in WEATHER_HOURLY_VARS:
            merged[v] = merged[v].astype(float) + w * merged[f"{v}_s"].astype(float)
            merged.drop(columns=[f"{v}_s"], inplace=True)
        out = merged

    out["temperature_2m"] = c_to_f(out["temperature_2m"])
    out["apparent_temperature"] = c_to_f(out["apparent_temperature"])
    out["region"] = region
    return out, meta


