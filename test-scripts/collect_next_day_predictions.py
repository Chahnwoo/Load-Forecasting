"""
collect_next_day_predictions.py

Daily pipeline to generate NEXT-DAY (day-ahead) *predicted* features for CAISO regions:
  regions = ['caiso', 'pge', 'sce', 'sdge', 'vea', 'mwd']

Output is a single dataframe (long format), one row per (region, hour UTC):
- time_key: YYYY-MM-DD:HH (UTC)
- time_utc: YYYY-MM-DD HH:MM:SS (UTC-naive, interpreted as UTC)
- weighted forecast weather features (Open-Meteo Forecast API, timezone=UTC)
- derived degree days (CDD/HDD) computed on Fahrenheit temperature
- predicted load (CAISO OASIS day-ahead load forecast, queryname=SLD_FCST, market_run_id=DAM)

Station weights:
- Prefer precomputed weights from:
    ./data/stations_population_weights.csv
  generated by build_station_population_weights.py
- Optional fallback to geocoding/deterministic stations if you pass --allow-fallback

Requirements satisfied:
- Modular functions
- All collection functions accept start_date/end_date
- Hourly scraping within time frame
- UTC-aligned to guarantee 24 hours/day

Dependencies:
  pip install pandas requests

Usage (recommended daily):
  python collect_next_day_predictions.py
  # default: target_date = tomorrow (UTC)

Or explicit:
  python collect_next_day_predictions.py --date 2024-01-08

If you need fallback behavior (not recommended for production):
  python collect_next_day_predictions.py --allow-fallback
"""

