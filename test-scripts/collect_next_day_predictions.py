def save_outputs(
    dataset: pd.DataFrame,
    station_meta: pd.DataFrame,
    *,
    start_date: date,
    end_date: date,
    out_dir: str = "./data",
) -> Tuple[str, str]:
    ensure_dir(out_dir)

    tag = f"{start_date.strftime('%Y%m%d')}_to_{end_date.strftime('%Y%m%d')}"
    p1 = os.path.join(out_dir, f"caiso_next_day_pred_features_{tag}.csv")
    p2 = os.path.join(out_dir, f"station_weights_next_day_{tag}.csv")

    out = dataset.copy()
    # Convert to UTC-aware then format as naive UTC string for CSV portability.
    ts = pd.to_datetime(out["time_utc"])
    if ts.dt.tz is not None:
        ts = ts.dt.tz_convert("UTC").dt.tz_localize(None)
    out["time_utc"] = ts.dt.strftime("%Y-%m-%d %H:%M:%S")
    out.to_csv(p1, index=False)
    station_meta.to_csv(p2, index=False)
    return p1, p2


