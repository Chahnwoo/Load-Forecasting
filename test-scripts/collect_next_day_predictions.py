def _oasis_dt_utc(d: datetime) -> str:
    return d.strftime("%Y%m%dT%H:%M-0000")

def _read_first_csv_from_zip(zbytes: bytes) -> pd.DataFrame:
    with zipfile.ZipFile(io.BytesIO(zbytes), "r") as zf:
        names = [n for n in zf.namelist() if n.lower().endswith(".csv")]
        if not names:
            raise RuntimeError("OASIS zip had no CSV files")
        with zf.open(names[0]) as f:
            return pd.read_csv(f)

def collect_caiso_day_ahead_load_forecast(
    *,
    start_date: date,
    end_date: date,
    debug: bool = True,
) -> pd.DataFrame:
    start_dt = datetime(start_date.year, start_date.month, start_date.day, 0, 0)
    end_excl = datetime(end_date.year, end_date.month, end_date.day, 0, 0) + timedelta(days=1)

    params = {
        "queryname": "SLD_FCST",
        "market_run_id": "DAM",
        "startdatetime": _oasis_dt_utc(start_dt),
        "enddatetime": _oasis_dt_utc(end_excl),
        "version": 1,
        "resultformat": 6,
    }

    if debug:
        print(f"[oasis_sld_fcst] requesting {params['startdatetime']} -> {params['enddatetime']}")

    zbytes = http_get_bytes(CAISO_OASIS_SINGLEZIP, params=params, timeout=120)
    df = _read_first_csv_from_zip(zbytes)

    start_col = None
    for c in ["INTERVALSTARTTIME_GMT", "INTERVALSTARTTIME", "INTERVAL_START_GMT", "INTERVAL_START_TIME_GMT"]:
        if c in df.columns:
            start_col = c
            break
    if start_col is None:
        raise RuntimeError(f"Could not find an interval start time column. Columns={list(df.columns)}")

    if "TAC_AREA_NAME" not in df.columns or "MW" not in df.columns:
        raise RuntimeError(f"Missing required columns TAC_AREA_NAME/MW. Columns={list(df.columns)}")

    # Keep timestamps as tz-aware UTC. Stripping tz before reindexing against a naive
    # index is the root cause of DST-boundary misalignment (pandas resolves the ambiguous
    # wall-clock hour incorrectly). We leave the tz attached so the reindex is exact.
    df["time_utc"] = pd.to_datetime(df[start_col], errors="coerce", utc=True).dt.tz_convert("UTC")
    df = df.dropna(subset=["time_utc"]).copy()

    df = df[df["TAC_AREA_NAME"].isin(TAC_TO_REGION.keys())].copy()
    df["region"] = df["TAC_AREA_NAME"].map(TAC_TO_REGION)
    df["MW"] = pd.to_numeric(df["MW"], errors="coerce")

    wide = df.pivot_table(index="time_utc", columns="region", values="MW", aggfunc="mean").reset_index()

    for r in ["caiso", "pge", "sce", "sdge", "vea", "mwd"]:
        if r not in wide.columns:
            wide[r] = pd.NA

    full_idx = utc_hourly_index(start_date, end_date)  # tz-aware UTC
    wide = wide.set_index("time_utc").reindex(full_idx).reset_index().rename(columns={"index": "time_utc"})

    if debug:
        present = {c: int(wide[c].notna().sum()) for c in ["caiso", "pge", "sce", "sdge", "vea", "mwd"]}
        print(f"[oasis_sld_fcst] non-null hours: {present}")

    return wide


