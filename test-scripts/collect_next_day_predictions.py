def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--date",
        default=None,
        help="Target date (UTC) YYYY-MM-DD for the 'next day' features. Default: tomorrow (UTC).",
    )
    ap.add_argument(
        "--regions",
        default=",".join(TARGET_REGIONS),
        help=f"Comma-separated regions. Default: {','.join(TARGET_REGIONS)}",
    )
    ap.add_argument(
        "--allow-fallback",
        action="store_true",
        help="If station weights CSV is missing/broken, fall back to geocoding/fallback coords (not recommended).",
    )
    ap.add_argument(
        "--historical-csv",
        type=str,
        default="./data/processed/filtered.csv",
        help="Path to processed historical CSV to look up load_previous_week.",
    )
    args = ap.parse_args()

    if args.date is None:
        from datetime import timezone
        target = datetime.now(tz=timezone.utc).date() + timedelta(days=1)
    else:
        target = parse_yyyy_mm_dd(args.date)

    start_date = target
    end_date = target

    regions = [r.strip().lower() for r in args.regions.split(",") if r.strip()]
    for r in regions:
        if r not in TARGET_REGIONS:
            raise ValueError(f"Unknown region '{r}'. Expected one of: {TARGET_REGIONS}")

    dataset, station_meta = build_dataset(
        regions, start_date=start_date, end_date=end_date, debug=True, allow_fallback=args.allow_fallback
    )
    
    dataset = apply_calendar_and_lag_features(dataset, args.historical_csv)
    
    p1, p2 = save_outputs(dataset, station_meta, start_date=start_date, end_date=end_date)

    expected = 24 * len(regions)
    if len(dataset) != expected:
        print(f"[warn] expected rows={expected}, got={len(dataset)} (check OASIS/weather availability)")

    print(f"Saved dataset: {p1}")
    print(f"Saved station weights: {p2}")
    print(f"Rows: {len(dataset):,} | Columns: {len(dataset.columns)}")

if __name__ == "__main__":
    main()