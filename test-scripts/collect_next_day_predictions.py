@dataclass(frozen=True)
class GeoStation:
    name: str
    latitude: float
    longitude: float
    population: float

def geocode_place(place_query: str) -> Optional[GeoStation]:
    js = http_get_json(
        OPEN_METEO_GEOCODE_URL,
        params={"name": place_query, "count": 1, "language": "en", "format": "json"},
        timeout=30,
    )
    results = js.get("results") or []
    if not results:
        return None
    r0 = results[0]
    pop = float(r0.get("population") or 0.0)
    return GeoStation(
        name=str(r0.get("name") or place_query),
        latitude=float(r0["latitude"]),
        longitude=float(r0["longitude"]),
        population=pop,
    )

def collect_region_stations_fallback(
    region: str, *, start_date: date, end_date: date, max_stations: int = 5
) -> Tuple[List[Station], List[float], pd.DataFrame]:
    """
    Fallback method if the weights CSV is unavailable. Uses Open-Meteo geocoding population;
    if population is missing, weights will become uniform.
    """
    _ = (start_date, end_date)

    qs = REGION_PLACE_QUERIES.get(region, [])[:max_stations]
    geo: List[GeoStation] = []
    for q in qs:
        try:
            st = geocode_place(q)
        except Exception:
            st = None
        if st is not None:
            geo.append(st)

    if not geo:
        fb = REGION_STATION_FALLBACKS.get(region, [])[:max_stations]
        if not fb:
            raise RuntimeError(f"No fallback coordinates configured for region={region}")
        stations = [Station(name=n, latitude=lat, longitude=lon) for (n, lat, lon) in fb]
        weights = [1.0 / len(stations)] * len(stations)
        meta = pd.DataFrame([{
            "region": region,
            "station_name": s.name,
            "latitude": s.latitude,
            "longitude": s.longitude,
            "population_weight": w,
            "population_raw": 0.0,
            "source": "fallback_coords_uniform",
        } for s, w in zip(stations, weights)])
        return stations, weights, meta

    pops = [max(0.0, s.population) for s in geo]
    weights = _normalize_weights(pops)
    stations = [Station(name=s.name, latitude=s.latitude, longitude=s.longitude) for s in geo]
    meta = pd.DataFrame([{
        "region": region,
        "station_name": s.name,
        "latitude": s.latitude,
        "longitude": s.longitude,
        "population_weight": w,
        "population_raw": s.population,
        "source": "open_meteo_geocode",
    } for s, w in zip(geo, weights)])
    return stations, weights, meta


