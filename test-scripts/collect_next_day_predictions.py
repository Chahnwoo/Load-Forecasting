def add_degree_days_f(df: pd.DataFrame) -> pd.DataFrame:
    t = df["temperature_2m"].astype(float)
    df["cdd_65f"] = (t - 65.0).clip(lower=0.0)
    df["hdd_65f"] = (65.0 - t).clip(lower=0.0)
    return df

def build_region_frame(
    region: str,
    *,
    start_date: date,
    end_date: date,
    load_pred_wide: pd.DataFrame,
    allow_fallback: bool,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    weather, meta = collect_weighted_region_weather_forecast(
        region, start_date=start_date, end_date=end_date, allow_fallback=allow_fallback
    )

    load_col = region.lower()
    if load_col in load_pred_wide.columns:
        merged = weather.merge(load_pred_wide[["time_utc", load_col]], on="time_utc", how="left")
        merged.rename(columns={load_col: "load_pred_mw"}, inplace=True)
    else:
        merged = weather.copy()
        merged["load_pred_mw"] = pd.NA

    merged["load_pred_mw"] = pd.to_numeric(merged["load_pred_mw"], errors="coerce")

    merged = add_degree_days_f(merged)
    merged["time_key"] = merged["time_utc"].apply(time_key_utc)

    keep = (
        ["region", "time_key", "time_utc"]
        + WEATHER_HOURLY_VARS
        + ["cdd_65f", "hdd_65f", "load_pred_mw"]
    )
    return merged[keep].copy(), meta

def build_dataset(
    regions: List[str],
    *,
    start_date: date,
    end_date: date,
    debug: bool = True,
    allow_fallback: bool = False,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    load_pred = collect_caiso_day_ahead_load_forecast(start_date=start_date, end_date=end_date, debug=debug)

    parts: List[pd.DataFrame] = []
    metas: List[pd.DataFrame] = []

    for r in regions:
        df_r, meta_r = build_region_frame(
            r, start_date=start_date, end_date=end_date, load_pred_wide=load_pred, allow_fallback=allow_fallback
        )
        parts.append(df_r)
        metas.append(meta_r)

    dataset = pd.concat(parts, ignore_index=True)
    station_meta = pd.concat(metas, ignore_index=True)

    dataset.sort_values(["region", "time_utc"], inplace=True)
    return dataset, station_meta

def apply_calendar_and_lag_features(dataset: pd.DataFrame, historical_csv: str) -> pd.DataFrame:
    df = dataset.copy()
    
    # Calendar features
    df["time_utc"] = pd.to_datetime(df["time_utc"], utc=True)
    df["is_weekend"] = (df["time_utc"].dt.dayofweek >= 5).astype(int)
    
    fed_cal = USFederalHolidayCalendar()
    fed_holidays = fed_cal.holidays(start=df["time_utc"].min(), end=df["time_utc"].max())
    # Note: Using tz-naive normalize for holiday matching
    df["US_federal_holidays"] = df["time_utc"].dt.tz_localize(None).dt.normalize().isin(fed_holidays).astype(int)
    
    ca_cal = CaliforniaHolidayCalendar()
    state_holidays = ca_cal.holidays(start=df["time_utc"].min(), end=df["time_utc"].max())
    df["state_holidays"] = df["time_utc"].dt.tz_localize(None).dt.normalize().isin(state_holidays).astype(int)
    
    # Lag feature from historical CSV
    df["load_previous_week"] = pd.NA
    if historical_csv and os.path.exists(historical_csv):
        hist_df = pd.read_csv(historical_csv)
        hist_df["time_utc"] = pd.to_datetime(hist_df["time_utc"], utc=True)
        # Shift time_utc forward by 7 days so we can merge on exact hour
        hist_df["time_utc"] = hist_df["time_utc"] + pd.Timedelta(days=7)
        hist_df = hist_df[["region", "time_utc", "load_mw"]].rename(columns={"load_mw": "load_previous_week"})
        
        df = df.drop(columns=["load_previous_week"]).merge(hist_df, on=["region", "time_utc"], how="left")
    else:
        print(f"[warn] historical_csv not provided or not found ({historical_csv}). load_previous_week will be NaN.")
        
    return df



